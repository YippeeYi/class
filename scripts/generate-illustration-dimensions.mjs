#!/usr/bin/env node

import { readdir, readFile, stat, writeFile } from 'node:fs/promises';
import { dirname, extname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const dataRoot = resolve(root, 'data');
const outputFile = resolve(root, 'js/illustrationDimensions.js');
const checkOnly = process.argv.includes('--check');

async function listFiles(directory) {
    const entries = await readdir(directory, { withFileTypes: true });
    const files = await Promise.all(entries.map((entry) => {
        const path = resolve(directory, entry.name);
        return entry.isDirectory() ? listFiles(path) : [path];
    }));
    return files.flat();
}

function readJpegDimensions(buffer) {
    if (buffer[0] !== 0xff || buffer[1] !== 0xd8) return null;
    let offset = 2;
    while (offset + 8 < buffer.length) {
        if (buffer[offset] !== 0xff) {
            offset += 1;
            continue;
        }
        const marker = buffer[offset + 1];
        if (marker === 0xd8 || marker === 0xd9) {
            offset += 2;
            continue;
        }
        const length = buffer.readUInt16BE(offset + 2);
        if (length < 2 || offset + length + 2 > buffer.length) return null;
        if ([0xc0, 0xc1, 0xc2, 0xc3, 0xc5, 0xc6, 0xc7, 0xc9, 0xca, 0xcb, 0xcd, 0xce, 0xcf].includes(marker)) {
            return { width: buffer.readUInt16BE(offset + 7), height: buffer.readUInt16BE(offset + 5) };
        }
        offset += length + 2;
    }
    return null;
}

function readImageDimensions(buffer, extension) {
    if (extension === '.jpg' || extension === '.jpeg') return readJpegDimensions(buffer);
    if (extension === '.png' && buffer.length >= 24 && buffer.toString('ascii', 1, 4) === 'PNG') {
        return { width: buffer.readUInt32BE(16), height: buffer.readUInt32BE(20) };
    }
    if (extension === '.gif' && buffer.length >= 10) {
        return { width: buffer.readUInt16LE(6), height: buffer.readUInt16LE(8) };
    }
    return null;
}

function resourceId(value) {
    let hash = 0x811c9dc5;
    for (const byte of Buffer.from(String(value), 'utf8')) {
        hash ^= byte;
        hash = Math.imul(hash, 0x01000193) >>> 0;
    }
    return `resource_${hash.toString(36).padStart(7, '0')}`;
}

const jsonFiles = (await listFiles(dataRoot)).filter((file) => extname(file).toLowerCase() === '.json');
const names = new Set();
for (const file of jsonFiles) {
    const source = await readFile(file, 'utf8');
    for (const match of source.matchAll(/\[\[illu:([^|\]\r\n]+)\|/g)) {
        const name = match[1].trim();
        if (name && !name.includes('/') && !name.includes('\\')) names.add(name);
    }
}

const dimensions = {};
const metadataErrors = [];
for (const name of [...names].sort((a, b) => a.localeCompare(b, 'en'))) {
    const file = resolve(dataRoot, 'attachments', name);
    try {
        const info = await stat(file);
        if (!info.isFile()) throw new Error('not a file');
        const size = readImageDimensions(await readFile(file), extname(name).toLowerCase());
        if (!size?.width || !size?.height) throw new Error('unsupported or invalid image header');
        const id = resourceId(`data/attachments/${name}`);
        if (dimensions[id]) throw new Error(`Illustration resource ID collision: ${id}`);
        dimensions[id] = size;
    } catch (error) {
        metadataErrors.push(`${relative(root, file)} (${error.message})`);
    }
}
if (metadataErrors.length) throw new Error(`Illustration metadata generation failed:\n${metadataErrors.join('\n')}`);

const output = `/* Generated by scripts/generate-illustration-dimensions.mjs. */\nwindow.ClassRecordIllustrationDimensions = Object.freeze(${JSON.stringify(dimensions, null, 2)});\n`;
if (checkOnly) {
    const current = await readFile(outputFile, 'utf8').catch(() => '');
    if (current !== output) throw new Error('js/illustrationDimensions.js is stale. Run npm run generate-illustration-dimensions.');
} else {
    await writeFile(outputFile, output, 'utf8');
    console.log(`Wrote ${Object.keys(dimensions).length} illustration dimensions.`);
}
