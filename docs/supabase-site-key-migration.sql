-- Reactions are kept, but are no longer tied to Supabase Auth users.
alter table public.record_reactions add column if not exists actor_key text;
alter table public.record_reactions alter column user_id drop not null;

delete from public.record_reactions
where type = 'favorite';

alter table public.record_reactions drop constraint if exists record_reactions_type_check;
alter table public.record_reactions
    add constraint record_reactions_type_check
    check (type in ('like', 'happy', 'surprised', 'sad', 'angry'));

create unique index if not exists record_reactions_record_actor_type_idx
on public.record_reactions (record_key, actor_key, type)
where actor_key is not null;

alter table public.record_reactions enable row level security;

drop policy if exists "reactions_select_authenticated" on public.record_reactions;
drop policy if exists "reactions_insert_self" on public.record_reactions;
drop policy if exists "reactions_delete_self" on public.record_reactions;
drop policy if exists "reactions_select_anon" on public.record_reactions;
drop policy if exists "reactions_insert_anon" on public.record_reactions;
drop policy if exists "reactions_delete_anon" on public.record_reactions;

create policy "reactions_select_anon"
on public.record_reactions for select
to anon
using (type in ('like', 'happy', 'surprised', 'sad', 'angry'));

create policy "reactions_insert_anon"
on public.record_reactions for insert
to anon
with check (
    actor_key is not null
    and length(actor_key) between 8 and 120
    and type in ('like', 'happy', 'surprised', 'sad', 'angry')
);

create policy "reactions_delete_anon"
on public.record_reactions for delete
to anon
using (actor_key is not null);

