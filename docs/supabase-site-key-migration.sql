-- Site-key access migration for the no-account frontend.
-- Run in Supabase SQL editor. Replace the sample key before inserting.

create extension if not exists pgcrypto;

create table if not exists public.site_keys (
    id bigint generated by default as identity primary key,
    key_hash text not null,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

alter table public.site_keys enable row level security;

drop policy if exists "site_keys_no_direct_select" on public.site_keys;
create policy "site_keys_no_direct_select"
on public.site_keys for select
to anon, authenticated
using (false);

create or replace function public.verify_site_key(input_key text)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
    return exists (
        select 1
        from public.site_keys sk
        where sk.is_active = true
          and sk.key_hash = crypt(input_key, sk.key_hash)
    );
end;
$$;

revoke all on function public.verify_site_key(text) from public;
grant execute on function public.verify_site_key(text) to anon, authenticated;

